export const TOP_500_EMOJIS: EmojiOption[] = RAW_EMOJI_OPTIONS.map((entry) => ({
  ...entry,
  categoryLabel: CATEGORY_LABEL_BY_KEY[entry.categoryKey]
}));

export function filterEmojiEntries(query: string): EmojiOption[] {
  const normalizedQuery = query.trim().toLowerCase();
  if (!normalizedQuery) {
    return TOP_500_EMOJIS;
  }
  return TOP_500_EMOJIS.filter((entry) => {
    const haystack = `${entry.label} ${entry.categoryLabel}`.toLowerCase();
    return haystack.includes(normalizedQuery);
  });
}

export function groupEmojiEntries(entries: readonly EmojiOption[]): EmojiGroup[] {
  return EMOJI_CATEGORIES.map((category) => ({
    key: category.key,
    label: category.label,
    icon: category.icon,
    emojis: entries.filter((entry) => entry.categoryKey === category.key)
  })).filter((group) => group.emojis.length > 0);
}

export function getEmojiEntryByValue(emoji: string): EmojiOption | null {
  const normalizedEmoji = emoji.trim();
  if (!normalizedEmoji) {
    return null;
  }

  return TOP_500_EMOJIS.find((entry) => entry.emoji === normalizedEmoji) ?? null;
}

